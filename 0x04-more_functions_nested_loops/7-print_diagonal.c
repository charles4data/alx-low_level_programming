#include "main.h"
#include <stdio.h>
#include <stdlib.h>

/**
 * print_diagonal - draws a diagonal line on the terminal
 * @n: parameter
 * Return: return 0
 */

void print_diagonal(int n)
{
	int i, j;

	if (n > 0)
	{
		for (i = 0; i < n; i++)
		{
			for (j = 0; i < n; i++)
			{
				if (j == i)
				{
					_putchar('\\');
				}
				else if (j < i)
				{
					_putchar(' ');
				}
			}
			_putchar('\n');
		}
	}
	else
	{
		_putchar('\n');
	}
}
